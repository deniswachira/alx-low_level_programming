/**
 * add - funciton that adds two ints
 * @a: integer
 * @b: integer
 * Return: sum
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - funciton that subtracts two ints
 * @a: integer
 * @b: integer
 * Return: difference
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - funciton that multiplies two ints
 * @a: integer
 * @b: integer
 * Return: multiple
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - funciton that divides two ints
 * @a: integer
 * @b: integer
 * Return: division
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - funciton that modulo two ints
 * @a: integer
 * @b: integer
 * Return: remainder
 */
int mod(int a, int b)
{
	return (a % b);
}
